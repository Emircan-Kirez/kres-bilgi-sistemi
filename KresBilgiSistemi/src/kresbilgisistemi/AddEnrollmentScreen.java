    public void changeLblPriceInfo(){
        try{
            String selectedEnrollmentType = (String) cBoxEnrollmentType.getSelectedItem();
            PreparedStatement preparedStatement = MainScreen.connection.prepareStatement("SELECT price FROM enrollmentPrice WHERE type = ?");
            preparedStatement.setString(1, selectedEnrollmentType);
            ResultSet resultSet = preparedStatement.executeQuery();
            while(resultSet.next()){
                lblPriceInfo.setText(String.format("%7.2f", resultSet.getFloat(1)));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
