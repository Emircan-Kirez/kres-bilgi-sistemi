    public void addEnrollment(){
        try{
            if(txtChildName.getText().equals("") || txtChildSurname.getText().equals("") || fTxtBirthdate.getText().contains(" ") || txtParentName.getText().equals("")
                    || fTxtPhone.getText().contains(" ")){
                JOptionPane.showMessageDialog(this, "Bütün bilgileri eksiksiz giriniz.");
            }else{
                MainScreen.connection.setAutoCommit(false); // aşağıdaki her bir query'nin çalışması lazım. O yüzden elle commit yapılır.
                
                PreparedStatement preparedStatement = MainScreen.connection.prepareStatement("INSERT INTO child VALUES  (nextval('childSeq'), ?, ?, ?)");
                preparedStatement.setString(1, txtChildName.getText());
                preparedStatement.setString(2,  txtChildSurname.getText());
                java.util.Date utilDate = formatter.parse(fTxtBirthdate.getText());
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                preparedStatement.setDate(3, sqlDate);
                preparedStatement.executeUpdate();
                
                preparedStatement = MainScreen.connection.prepareStatement("SELECT MAX(id) FROM child");
                ResultSet resultSet = preparedStatement.executeQuery();
                int childId = 0;
                while(resultSet.next()){
                    childId = resultSet.getInt(1);
                }
                
                preparedStatement = MainScreen.connection.prepareStatement("INSERT INTO enrollment VALUES (?, ?, ?, (SELECT EXTRACT(YEAR FROM age(current_date, birthdate)) FROM child WHERE id = ?))");
                preparedStatement.setInt(1, childId);
                preparedStatement.setString(2, (String) cBoxPaymentType.getSelectedItem());
                preparedStatement.setString(3, (String) cBoxEnrollmentType.getSelectedItem());
                preparedStatement.setInt(4, childId);
                preparedStatement.executeUpdate();
                
                
                preparedStatement = MainScreen.connection.prepareStatement("INSERT INTO parent (childId, fname, lname, gender, phone) VALUES (?, ?, ?, ?, ?)");
                preparedStatement.setInt(1, childId);
                preparedStatement.setString(2, txtParentName.getText());
                preparedStatement.setString(3, txtChildSurname.getText());
                preparedStatement.setString(4, (String) cBoxGender.getSelectedItem());
                preparedStatement.setString(5, fTxtPhone.getText());
                preparedStatement.executeUpdate();
                MainScreen.connection.commit();
                
                JOptionPane.showMessageDialog(this, "Kayıt başarıyla oluşturuldu", "Çocuk Ekleme", JOptionPane.INFORMATION_MESSAGE);
                JOptionPane.showMessageDialog(this, "triggerIncreaseSizeOfClass triggerı çalıştı ve ilgili sınıf mevcudu bir arttırıldı.", "Trigger Mesajı", JOptionPane.INFORMATION_MESSAGE);
                JOptionPane.showMessageDialog(this, "triggerRelationshipType triggerı çalıştı ve yakınlık derecesi eklendi.", "Trigger Mesajı", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception e){
            try {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                MainScreen.connection.rollback();
            } catch (SQLException ex) {
                Logger.getLogger(AddEnrollmentScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }finally{
            try {
                MainScreen.connection.setAutoCommit(true);
            } catch (SQLException ex) {
                Logger.getLogger(AddEnrollmentScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }

