    public void populate(){
        model = (DefaultTableModel)tableParentInfo.getModel();
        model.setRowCount(0);
        try{
            
            PreparedStatement preparedStatement = MainScreen.connection.prepareStatement("SELECT * FROM parent ORDER BY childId");
            ResultSet resultSet = preparedStatement.executeQuery();
            Object[] row = new Object[6];
            while(resultSet.next()){
                for (int i = 0; i < 6; i++){
                    row[i] = resultSet.getObject(i+1);
                }
                model.addRow(row);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
       
        tableParentInfo.setModel(model);
    };

