    private void btnUpdateMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMousePressed
        try{
            if(txtId.getText().equals("") || fTxtPhone.getText().contains(" ")){
                JOptionPane.showMessageDialog(this, "Bütün bilgileri eksiksiz giriniz.");
            }else{
                PreparedStatement preparedStatement = MainScreen.connection.prepareStatement("UPDATE parent SET phone = ? WHERE childId = ?");
                preparedStatement.setString(1, fTxtPhone.getText());
                preparedStatement.setInt(2, Integer.valueOf(txtId.getText()));
                int result = preparedStatement.executeUpdate();
                populate();
                if(result != 0)
                    JOptionPane.showMessageDialog(this, "Telefon numarası başarıyla güncellendi.");
                else
                    JOptionPane.showMessageDialog(this, "Geçersiz bir id girdiniz.");
                txtId.setText("");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnUpdateMousePressed

